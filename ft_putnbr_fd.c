#include <unistd.h>

#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		write (fd, "-", 1);
		n = n * -1;
	}
	if (n >= 10)
	{
		write (fd, &"0123456789"[n / 10], fd);
		write (fd, &"0123456789"[n % 10 + '0'], fd);
	}
}
