#include <stdio.h>
#include <string.h>
#include <stddef.h>

#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((unsigned char *)s)[i] = '\0';
		i++;
	}
}

int main()
{
	char	c[100] = "abcdefghisdklsdsfg";
	ft_bzero(c, 10);
}